package com.green.day6.ch4;

public class While {
    public static void main(String[] args) {
        //while은 반복문입니다.

        for(int i=0; i<10;) {
            System.out.println("for-"+  i++);
        }

        System.out.println("-----------");
        int i=0;
        while(i<10) {
            System.out.println("while-" + i++);

        }
        //for 횟수 정확히알때
            //++쓸때
        // 언제 멈출지 조건을 알때 while
            //원하는값이 나올때 까지 반복 while
        //
    }
}
